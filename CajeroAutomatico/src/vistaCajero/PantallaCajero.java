    private void retirarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_retirarActionPerformed
        int retirador = Integer.parseInt((JOptionPane.showInputDialog("Ingresa la cantidad de dinero a retirar")));
        
        if(retirador > this.usuario.dinero){
            while(retirador > this.usuario.dinero){
                JOptionPane.showMessageDialog(null, "NO PUEDES RETIRAR MÁS DE LO QUE POSEES!!");
                retirador = Integer.parseInt(JOptionPane.showInputDialog("Ingresa la cantidad de dinero a retirar"));
            }
            int total = (this.usuario.dinero) - retirador;
            this.usuario.setDinero(total);
            this.retirado.setText("VALOR RETIRADO: " + Integer.toString(retirador));
        }else{
            int total = (this.usuario.dinero) - retirador;
            this.usuario.setDinero(total);
            this.retirado.setText("VALOR RETIRADO: " + Integer.toString(retirador));
        }
    }//GEN-LAST:event_retirarActionPerformed

