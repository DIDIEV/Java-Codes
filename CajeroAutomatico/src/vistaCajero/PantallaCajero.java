    private void cambiadorFechaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cambiadorFechaMouseClicked
        
        this.usuario.fechaActual = this.usuario.fechaActual.plusDays(1);
        this.cambiadorFecha.setText(this.usuario.fechaActual.toString());
        
        if((diaInicio + 30) == this.usuario.fechaActual.getDayOfYear() && this.usuario.prestamo != 0){
            Eleccion pagador = new Eleccion(this.usuario,2);
            
            pagador.setVisible(true);
            pagador.setLocationRelativeTo(null);
            diaInicio += 30;
        }
        
    }//GEN-LAST:event_cambiadorFechaMouseClicked


