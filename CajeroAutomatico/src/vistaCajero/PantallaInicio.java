    private void BtnIngresoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BtnIngresoMouseClicked
        
        
        if(this.CampoTarjeta.getText().equals(this.usuario.tarjeta) && !this.campoIdentidad.getText().equals("Ingrese su cédula") && !this.campoIdentidad.getText().equals("")){
            this.usuario.nombre = this.campoNombre.getText();
            this.usuario.apellido = this.campoApellido.getText();
            this.usuario.identidad = this.campoIdentidad.getText();
            
            Login ingreso = new Login(this.usuario,1);
            
            ingreso.setVisible(true);
            ingreso.setLocationRelativeTo(null);
            this.dispose();
        }else{
            this.avisoFallo.setText("Parece que hubo un error, ingresa nuevamente los datos");
        }
    }//GEN-LAST:event_BtnIngresoMouseClicked

