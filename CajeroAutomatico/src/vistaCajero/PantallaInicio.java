    private void CampoTarjetaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_CampoTarjetaKeyTyped
        
        if(this.CampoTarjeta.getText().equals("Ingrese su tarjeta")){
            this.CampoTarjeta.setText("");
        }
        
        if(this.CampoTarjeta.getText().length() >= 4){
            evt.consume();
        }else{
            char c = evt.getKeyChar();
            if (c < '0' || c > '9' ){
                evt.consume();
            }
        }
    }//GEN-LAST:event_CampoTarjetaKeyTyped

    private void campoIdentidadKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_campoIdentidadKeyTyped
        
        if(this.campoIdentidad.getText().equals("Ingrese su cédula")){
            this.campoIdentidad.setText("");
        }
        
        if(this.campoIdentidad.getText().length() >= 10){
            evt.consume();
        }else{
            char c = evt.getKeyChar();
            if (c < '0' || c > '9' ){
                evt.consume();
            }
        }
    }//GEN-LAST:event_campoIdentidadKeyTyped

    private void campoNombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_campoNombreKeyTyped
        
        if(this.campoNombre.getText().equals("Ingrese su nombre")){
            this.campoNombre.setText("");
        }
        
        char c = evt.getKeyChar();
        if (!Character.isLetter(c) && c != ' ') {
            evt.consume();
        }
    }//GEN-LAST:event_campoNombreKeyTyped

