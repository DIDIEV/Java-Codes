    private void valPrestamoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_valPrestamoKeyTyped
        
        if(this.valPrestamo.getText().equals("Valor del préstamo")){
            this.valPrestamo.setText("");
        }
        
        if(valPrestamo.getText().length() >= 10){
            evt.consume();
        }else{
            char c = evt.getKeyChar();
            if (c < '0' || c > '9' ){
                evt.consume();
            }
        }
         
    }//GEN-LAST:event_valPrestamoKeyTyped

