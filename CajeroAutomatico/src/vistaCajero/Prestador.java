    private void confirmacionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_confirmacionMouseClicked
        
        Prestamos nuevoPrestamo = new Prestamos(this.usuario,0);
        nuevoPrestamo.setNumeroPrestamo(this.usuario.totalPrestamos);

        if (this.usuario.maxPrestamo > Integer.parseInt(this.valPrestamo.getText())){
            
            nuevoPrestamo.ingresarDinero(Integer.parseInt(this.valPrestamo.getText()));
            this.dispose();
            this.usuario.maxPrestamo = (this.usuario.maxPrestamo - Integer.parseInt(this.valPrestamo.getText()));
            JOptionPane.showMessageDialog(null, "Nombre: " + this.usuario.nombre + "\n" + "Apellidos: " + this.usuario.apellido + "\n" + "Documento número: " + this.Identidad.getText() + "\n" +"Numero de celular: " + this.Celular.getText()+ "\n" + "Numero de teléfono: " +this.Telefono.getText() + "\n" + "Valor de préstamo: " +  this.valPrestamo.getText() + "\n" + this.fechaActual.getText() + "     " +  this.fechaMaxima.getText());
            
            
        }else if(this.usuario.maxPrestamo < Integer.parseInt(this.valPrestamo.getText())){
            
            this.sliderWarning.setText("!No tienes pemitido un prestamo así de grande¡");
        }else{
            nuevoPrestamo.ingresarDinero(Integer.parseInt(this.valPrestamo.getText()));
            this.dispose();
            this.usuario.maxPrestamo = (this.usuario.maxPrestamo - Integer.parseInt(this.valPrestamo.getText()));
            JOptionPane.showMessageDialog(null, "Nombre: " + this.usuario.nombre + "\n" + "Apellidos: " + this.usuario.apellido + "\n" + "Documento número: " + this.Identidad.getText() + "\n" +"Numero de celular: " + this.Celular.getText()+ "\n" + "Numero de teléfono: " +this.Telefono.getText() + "\n" + "Valor de préstamo: " +  this.valPrestamo.getText() + "\n" + this.fechaActual.getText() + "     " +  this.fechaMaxima.getText());
            nuevoPrestamo.ingresarDinero(Integer.parseInt(this.valPrestamo.getText()));
        }
        
        
        
    }//GEN-LAST:event_confirmacionMouseClicked

